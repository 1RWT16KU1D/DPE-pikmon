u32 __attribute__((long_call)) GetMonData(void*, s32, const void *data);
void __attribute__((long_call)) break_func(u32);
#define MON_DATA_SPECIES           11
#define EGG_MOVES_ARRAY_COUNT 50
// Counts the number of egg moves a pokemon learns and stores the moves in
// the given array.
u8 GetEggMoves(void* pokemon, u16 *eggMoves)
{
	u16 numEggMoves;
	u16 species;
	u32 i;
	u32 eggMoveIdx;

	numEggMoves = 0;
	eggMoveIdx = 0;
	species = GetMonData(pokemon, MON_DATA_SPECIES, 0);

	for (i = 0; i < ARRAY_COUNT(gEggMoves) - 1; ++i)
	{
		if (gEggMoves[i] == species + EGG_MOVES_SPECIES_OFFSET)
		{
			eggMoveIdx = i + 1;
			break;
		}
	}

	for (i = 0; i < EGG_MOVES_ARRAY_COUNT; ++i)
	{
		if (gEggMoves[eggMoveIdx + i] > EGG_MOVES_SPECIES_OFFSET)
			break;
	
		eggMoves[i] = gEggMoves[eggMoveIdx + i];
		numEggMoves++;
	}

	return numEggMoves;
}

extern const struct Evolution gEvolutionTable[NUM_SPECIES][EVOS_PER_MON];
// Determines what the species of an Egg would be based on the given species.
// It determines this by working backwards through the evolution chain of the
// given species.
u16 GetEggSpecies(u16 species)
{
	int i, j, k;
	bool8 found;

	for (i = 0; i < 3; ++i)
	{
		found = FALSE;

		for (j = 1; j < NUM_SPECIES; ++j)
		{
			for (k = 0; k < EVOS_PER_MON; ++k)
			{
				if (gEvolutionTable[j][k].method != EVO_MEGA
				&& gEvolutionTable[j][k].method != EVO_GIGANTAMAX
				&& gEvolutionTable[j][k].targetSpecies == species)
				{
					species = j;
					found = TRUE;
					break;
				}

				if (gEvolutionTable[j][k].method == EVO_NONE) //Most likely end of entries
					break; //Break now to save time
			}

			if (found)
				break;
		}

		if (j == NUM_SPECIES)
			break;
	}

	return species;
}
